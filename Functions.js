/**        
 *      Funtions
 */

function add(a, b) {
    return a + b
}

function subtract(a, b) {
    return a - b
}

function multiply(a, b) {
    return a * b
}

function divide(a, b) {
    return a / b
}

console.log(add(3, 5))          //8
console.log(add(4, 7))          //11
console.log(subtract(8, 4))     //4
console.log(subtract(6, 4))     //2
console.log(multiply(3, 4))     //12
console.log(multiply(8, 9))     //72
console.log(divide(16, 4))      //4
console.log(divide(18, 9))      //2

