def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

if __name__ == "__main__":
    print(add(5, 7))            #12
    print(add(9, 15))           #24
    print(subtract(13, 4))      #9
    print(subtract(5, 1))       #4
    print(multiply(11, 3))      #33
    print(multiply(7, 9))       #63
    print(divide(12, 4))        #3
    print(divide(15, 3))        #5

    